function [asset]=asset_caculation
    
    asset=zeros(1,13);
    %因素集权重
    U_AH=[0.6, 0.25, 0.15];
    U_ES=[0.3, 0.35, 0.35];
    U_CC=[0.2, 0.3, 0.5];
    U_VA=[0.1, 0.3, 0.6];
    U_IS=[0.2, 0.3, 0.5];

    %隶属度矩阵
    R_AH=   [0.5, 0.3, 0.15, 0.05, 0;
             0.3, 0.3, 0.2, 0.1, 0.1;
             0.2, 0.3, 0.3, 0.1, 0.1];
    R_ES=   [0.3, 0.3, 0.2, 0.2, 0;
             0.5, 0.3, 0.2, 0, 0;
             0.6, 0.3, 0.1, 0, 0];
    R_CC=   [0.7, 0.2, 0.1, 0, 0;
             0.9, 0.1, 0, 0, 0;
             0.9, 0.1, 0, 0, 0];
    R_VA=   [0.1, 0.2, 0.2, 0.3, 0.2;
             0.5, 0.3, 0.1, 0.1, 0;
             0.8, 0.2, 0, 0, 0];
    R_IS=   [0.7, 0.2, 0.1, 0, 0;
             0.8, 0.15, 0.05, 0, 0;
             0.9, 0.1, 0, 0, 0];
%     R_IS=   [0.4, 0.3, 0.2, 0.1, 0;
%              0.4, 0.3, 0.2, 0.1, 0;
%              0.4, 0.3, 0.2, 0.1, 0];

    %综合评定向量
    V_AH = U_AH * R_AH;
    V_ES = U_ES * R_ES;
    V_CC = U_CC * R_CC;
    V_VA = U_VA * R_VA;
    V_IS = U_IS * R_IS;

    %资产重要性量化值
    Y=[5,4,3,2,1];
    F_AH  =Y * V_AH';
    F_ES  =Y * V_ES';
    F_CC  =Y * V_CC';
    F_VA  =Y * V_VA';
    F_IS  =Y * V_IS';

    asset(1,1:2) = F_AH;
    asset(1,3:4) = F_ES;
    asset(1,5:7) = F_CC;
    asset(1,8:10) = F_IS;
    asset(1,11:13) = F_VA;
    
